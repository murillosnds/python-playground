seguidores = 1
while seguidores < 100:
    seguidores += 1
    if seguidores == 50:
        continue
    print(seguidores)

# Aqui usamos while (enquanto) e continue (continuar). A variável seguidores armazena o valor 1 e enquanto o números de seguidores for menor que 100 é somado de 1 em 1. Se o número de seguidores for igual a 50, o código continuará.

# Por fim é impresso o número de seguidores.